let person = 'Giselle Barbosa';

name = person.toUpperCase() // encapsulada a person na variavel name

console.log(name);
console.log(name.toLowerCase());

console.log(name.charAt(5));          // mostra quem esta no indece desejado [5]
console.log(name[5]);                 // mostra quem esta no indece desejado [5]
console.log(name.indexOf('G'));       // mostra o indice onde o caracter desejado esta 

console.log(name.slice(0,2));         // pega os caracteres dentro de um intervalo selecionado (index 0 ao 2) 
console.log(name.split(""));          // separa cada letra em um index 
console.log(name.split(" "));          // separa cada palavra em um index 



